import { ethers } from 'hardhat';

async function main() {
	const [deployer] = await ethers.getSigners();

	console.log('Deploying account:', await deployer.getAddress());
	console.log('Deploying account balance:', (await deployer.getBalance()).toString(), '\n');

	const NFTXLPStaking = await ethers.getContractFactory('NFTXLPStaking');
	const lpStaking = await NFTXLPStaking.deploy();
	await lpStaking.deployed();
	console.log('NFTXLPStaking impl:', lpStaking.address);
}

main()
	.then(() => {
		console.log('\nDeployment completed successfully ✓');
		process.exit(0);
	})
	.catch((error) => {
		console.log('\nDeployment failed ✗');
		console.error(error);
		process.exit(1);
	});
